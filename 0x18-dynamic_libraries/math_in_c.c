#include "main.h"
/**
  * add - add two integers
  * @a: first integer
  * @b: second integer
  *
  * Return: sum of integers
  */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of integers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of integers
 */
int mul(int a, int b)
{
	        return (a * b);
}

/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of integers
 */
int div(int a, int b)
{
	        return (a / b);
}

/**
 * mod - module two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of integers
 */
int mod(int a, int b)
{
	        return (a % b);
}

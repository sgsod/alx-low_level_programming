/**
  * op_add - adds 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: sum
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtract 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: difference
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiply 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: product
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divide 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: result of division of a & b
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - find remainder of division of 2 integers
  * @a: first integer
  * @b: second integer
  *
  * Return: remainder
  */
int op_mod(int a, int b)
{
	return (a % b);
}
